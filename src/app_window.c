/*
 * app_window.c — MainWindow, TextWindow, Sine Wave Animation, Countdown Timer,
 *                Status Bar, TextWindow Screen Boundary Awareness
 *
 * This module implements all GTK3 UI components for the Transcriber application:
 *
 *   1. MainWindow       — Primary application window with microphone icon,
 *                          sine wave animation overlay, and status bar.
 *   2. TextWindow       — Transient utility window for displaying/editing
 *                          transcribed text. Positioned below MainWindow by
 *                          default; if it would extend past the bottom of the
 *                          display monitor, it is positioned above instead.
 *   3. Sine Wave Animation — Cairo-driven animation on GtkDrawingArea during
 *                          STATE_LISTENING only. Rendered in blue (RGB 0,0,1)
 *                          with a line width of 6.0 pixels. Hidden during
 *                          STATE_IDLE and STATE_TRANSCRIBING.
 *   4. Countdown Timer  — GtkLabel in the center of the status bar that counts
 *                          down from max_duration to 0 during STATE_LISTENING.
 *                          Hidden during all other states.
 *   5. Status Bar       — 16-pixel bar with gear button (left), countdown timer
 *                          (center), and connection indicator (right).
 *   6. Config Callback  — Invoked when the Configuration Dialog saves changes,
 *                          allowing the main application to apply runtime config
 *                          updates (e.g., audio device selection).
 *
 * Architecture:
 *   - All GTK widgets are created and managed within this module
 *   - XPM icons loaded from assets/ directory at runtime
 *   - Cairo drawing for sine wave animation and connection indicator
 *   - Timer-driven animation at 30fps (33ms interval)
 *   - Countdown timer updates every 1 second via g_timeout_add_seconds()
 *   - Thread-safe state updates via GTK main context
 */

