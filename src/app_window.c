TextWindow *app_text_window_create(GtkWindow *main_window_gtk) {
    TextWindow *tw = (TextWindow *)calloc(1, sizeof(TextWindow));
    if (!tw) {
        return NULL;
    }

    /* Create the text window */
    tw->window = GTK_WINDOW(gtk_window_new(GTK_WINDOW_TOPLEVEL));
    if (!tw->window) {
        free(tw);
        return NULL;
    }

    /* Set window properties */
    gtk_window_set_title(tw->window, "Transcription");
    gtk_window_set_resizable(tw->window, TRUE);
    gtk_window_set_type_hint(tw->window, GDK_WINDOW_TYPE_HINT_UTILITY);
    /* Note: Not setting transient_for or destroy_with_parent because the main
     * window is hidden (tray app). Setting these causes the window manager to
     * send a delete-event to the TextWindow immediately after showing it. */

    /* Position below MainWindow, with screen boundary check */
    if (main_window_gtk && gtk_widget_get_visible(GTK_WIDGET(main_window_gtk))) {
        int mx, my, mw, mh;
        gtk_window_get_position(main_window_gtk, &mx, &my);
        gtk_window_get_size(main_window_gtk, &mw, &mh);

        /* Get TextWindow size */
        int tw_w, tw_h;
        gtk_window_get_size(tw->window, &tw_w, &tw_h);

        /* Try to position below MainWindow */
        int tx = mx;
        int ty = my + mh + TEXT_WINDOW_OFFSET_Y;

        /* Check if it would go off the bottom of the screen */
        GdkDisplay *display = gtk_widget_get_display(GTK_WIDGET(tw->window));
        GdkMonitor *mon = gdk_display_get_monitor_at_window(display, gtk_widget_get_window(GTK_WIDGET(tw->window)));
        GdkRectangle monitor;
        gdk_monitor_get_geometry(mon, &monitor);

        if (ty + tw_h > monitor.y + monitor.height) {
            /* Position above the MainWindow instead */
            ty = my - tw_h - TEXT_WINDOW_OFFSET_Y;
            if (ty < monitor.y) {
                ty = monitor.y;
            }
        }

        gtk_window_move(tw->window, tx, ty);
    }

    /* Set default size */
    gtk_window_set_default_size(tw->window, 400, 200);

    /* Create the text view */
    tw->text_view = GTK_TEXT_VIEW(gtk_text_view_new());
    gtk_text_view_set_wrap_mode(tw->text_view, GTK_WRAP_WORD_CHAR);
    gtk_text_view_set_editable(tw->text_view, TRUE);
    gtk_text_view_set_cursor_visible(tw->text_view, TRUE);
    /* Undo requires GtkSourceView (not available) */

    /* Get the text buffer */
    tw->buffer = gtk_text_view_get_buffer(tw->text_view);

    /* Add text view to a scrolled window */
    GtkScrolledWindow *scrolled = GTK_SCROLLED_WINDOW(gtk_scrolled_window_new(NULL, NULL));
    gtk_scrolled_window_set_policy(scrolled, GTK_POLICY_AUTOMATIC, GTK_POLICY_AUTOMATIC);
    gtk_container_add(GTK_CONTAINER(scrolled), GTK_WIDGET(tw->text_view));

    /* Add scrolled window to the text window */
    gtk_container_add(GTK_CONTAINER(tw->window), GTK_WIDGET(scrolled));

    /* Connect delete event to hide (not destroy) */
    g_signal_connect(tw->window, "delete-event",
                      G_CALLBACK(on_text_window_delete_event), tw);

    /* MIN-007 fix: Listen for MainWindow position changes */
    if (main_window_gtk) {
        g_signal_connect(main_window_gtk, "configure-event",
                         G_CALLBACK(on_main_window_configure_event), tw);
    }

    return tw;
}

void app_text_window_destroy(TextWindow *tw) {
    if (!tw) return;

    if (tw->window) {
        gtk_widget_destroy(GTK_WIDGET(tw->window));
    }

    free(tw);
}

void app_text_window_append_text(TextWindow *tw, const char *text) {
    if (!tw || !text) return;

    /* Append text to the buffer */
    GtkTextIter end;
    gtk_text_buffer_get_end_iter(tw->buffer, &end);
    gtk_text_buffer_insert(tw->buffer, &end, text, -1);
    gtk_text_buffer_insert(tw->buffer, &end, "\n", 1);

    /* Show and raise the window */
    gtk_widget_show_all(GTK_WIDGET(tw->window));
    gtk_window_present(tw->window);

    /* Scroll to the end */
    gtk_text_buffer_get_end_iter(tw->buffer, &end);
    gtk_text_view_scroll_to_iter(tw->text_view, &end, 0, FALSE, 0, 0);
}

void app_text_window_set_error(TextWindow *tw, const char *error) {
    if (!tw || !error) return;

    /* Prefix with "ERROR: " */
    GtkTextIter end;
    gtk_text_buffer_get_end_iter(tw->buffer, &end);
    gtk_text_buffer_insert(tw->buffer, &end, "ERROR: ", 7);
    gtk_text_buffer_insert(tw->buffer, &end, error, -1);
    gtk_text_buffer_insert(tw->buffer, &end, "\n", 1);

    /* Show and raise the window */
    gtk_widget_show_all(GTK_WIDGET(tw->window));
    gtk_window_present(tw->window);
}

