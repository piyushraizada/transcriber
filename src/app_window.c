MainWindow *app_window_create(AppConfig *config, AppStateController *controller,
                               WhisperClient *whisper_client) {
    MainWindow *win = (MainWindow *)calloc(1, sizeof(MainWindow));
    if (!win) {
        return NULL;
    }

    win->config = config;
    win->controller = controller;
    win->whisper_client = whisper_client;
    win->animation_source_id = 0;
    win->wave_phase = 0.0;
    win->icon_width = 0;
    win->icon_height = 0;
    win->cached_red_icon = NULL;
    win->cached_green_icon = NULL;
    win->checking_source_id = 0;
    win->checking_blink = FALSE;
    win->countdown_source_id = 0;
    win->countdown_seconds = 0;
    /* Default to TRUE — model has not loaded yet at window creation time.
     * Main() will set to FALSE once the model is fully loaded. */
    win->model_loading = TRUE;

    /* Determine asset directory */
    get_asset_dir(win);

    /* Create the main window */
    win->window = GTK_WINDOW(gtk_window_new(GTK_WINDOW_TOPLEVEL));
    if (!win->window) {
        free(win);
        return NULL;
    }

    /* Set window properties */
    gtk_window_set_title(win->window, "Transcriber");
    gtk_window_set_resizable(win->window, FALSE);
    gtk_window_set_type_hint(win->window, GDK_WINDOW_TYPE_HINT_NORMAL);

    /* Set the window icon (taskbar/dock icon) to the red mic */
    GdkPixbuf *window_icon = load_xpm(win, "redmic.xpm");
    if (window_icon) {
        gtk_window_set_icon(win->window, window_icon);
        g_object_unref(window_icon);
    }

    /* Create the main vertical box */
    win->main_box = GTK_BOX(gtk_box_new(GTK_ORIENTATION_VERTICAL, 0));
    gtk_container_add(GTK_CONTAINER(win->window), GTK_WIDGET(win->main_box));

    /* Create the icon drawing area */
    win->icon_area = GTK_DRAWING_AREA(gtk_drawing_area_new());
    gtk_widget_add_events(GTK_WIDGET(win->icon_area), GDK_BUTTON_PRESS_MASK);
    g_signal_connect(win->icon_area, "draw", G_CALLBACK(on_icon_draw), win);
    g_signal_connect(win->icon_area, "button-press-event",
                     G_CALLBACK(on_icon_button_press), win);

    /* Create the animation drawing area (stacked on top of icon area) */
    win->animation_area = GTK_DRAWING_AREA(gtk_drawing_area_new());
    gtk_widget_add_events(GTK_WIDGET(win->animation_area), GDK_BUTTON_PRESS_MASK);
    g_signal_connect(win->animation_area, "draw", G_CALLBACK(on_animation_draw), win);
    g_signal_connect(win->animation_area, "button-press-event",
                     G_CALLBACK(on_icon_button_press), win);

    /* Use a GtkOverlay to stack icon and animation drawing areas */
    GtkOverlay *overlay = GTK_OVERLAY(gtk_overlay_new());
    gtk_container_add(GTK_CONTAINER(overlay), GTK_WIDGET(win->icon_area));
    gtk_overlay_add_overlay(overlay, GTK_WIDGET(win->animation_area));
    gtk_widget_hide(GTK_WIDGET(win->animation_area));  // Hide until STATE_LISTENING
    gtk_box_pack_start(win->main_box, GTK_WIDGET(overlay), TRUE, TRUE, TRUE);

    /* Create the status bar */
    win->status_bar = GTK_BOX(gtk_box_new(GTK_ORIENTATION_HORIZONTAL, 0));
    gtk_widget_set_size_request(GTK_WIDGET(win->status_bar), -1, STATUS_BAR_HEIGHT);
    gtk_box_pack_start(win->main_box, GTK_WIDGET(win->status_bar), FALSE, FALSE, FALSE);

    /* Create the gear button */
    win->gear_button = GTK_BUTTON(gtk_button_new());
    GdkPixbuf *gear_pixbuf = load_xpm(win, "gear.xpm");
    if (gear_pixbuf) {
        /* Scale the gear icon to 16x16 per GNOME HIG for status bar icons */
        int dest_size = 16;
        GdkPixbuf *scaled = gdk_pixbuf_scale_simple(gear_pixbuf, dest_size, dest_size, GDK_INTERP_BILINEAR);
        GtkImage *gear_image = GTK_IMAGE(gtk_image_new_from_pixbuf(scaled));
        gtk_container_add(GTK_CONTAINER(win->gear_button), GTK_WIDGET(gear_image));
        g_object_unref(scaled);
        g_object_unref(gear_pixbuf);
    }
    gtk_widget_set_tooltip_text(GTK_WIDGET(win->gear_button), "Configuration");
    g_signal_connect(win->gear_button, "clicked", G_CALLBACK(on_gear_button_clicked), win);
    gtk_box_pack_start(win->status_bar, GTK_WIDGET(win->gear_button), FALSE, FALSE, 4);

    /* Expander to push countdown to center */
    GtkLabel *spacer_left = GTK_LABEL(gtk_label_new(""));
    gtk_box_pack_start(win->status_bar, GTK_WIDGET(spacer_left), TRUE, TRUE, 0);

    /* Add countdown timer label (centered in status bar) */
    win->countdown_label = GTK_LABEL(gtk_label_new(""));
    gtk_widget_set_no_show_all(GTK_WIDGET(win->countdown_label), FALSE);
    gtk_widget_hide(GTK_WIDGET(win->countdown_label));
    gtk_box_pack_start(win->status_bar, GTK_WIDGET(win->countdown_label), FALSE, FALSE, 0);

    /* Volume level bar (hidden until recording) */
    win->volume_level_bar = GTK_LEVEL_BAR(gtk_level_bar_new());
    gtk_level_bar_set_mode(win->volume_level_bar, GTK_LEVEL_BAR_MODE_CONTINUOUS);
    gtk_level_bar_set_min_value(win->volume_level_bar, 0.0);
    gtk_level_bar_set_max_value(win->volume_level_bar, 1.0);
    gtk_level_bar_set_value(win->volume_level_bar, 0.0);
    /* Add threshold markers: warning at 0.8, critical at 0.95 (clipping) */
    gtk_level_bar_add_offset_value(win->volume_level_bar, "warning", 0.8);
    gtk_level_bar_add_offset_value(win->volume_level_bar, "alert", 0.95);
    gtk_widget_set_size_request(GTK_WIDGET(win->volume_level_bar), 60, 6);
    gtk_widget_hide(GTK_WIDGET(win->volume_level_bar));
    gtk_box_pack_start(win->status_bar, GTK_WIDGET(win->volume_level_bar), FALSE, FALSE, 2);

    /* Expander to push indicator to the right */
    GtkLabel *spacer_right = GTK_LABEL(gtk_label_new(""));
    gtk_box_pack_start(win->status_bar, GTK_WIDGET(spacer_right), TRUE, TRUE, 0);

    /* Create the connection indicator drawing area */
    win->indicator_area = GTK_DRAWING_AREA(gtk_drawing_area_new());
    gtk_widget_set_size_request(GTK_WIDGET(win->indicator_area),
                                INDICATOR_SIZE + 8,
                                INDICATOR_SIZE + 8);
    g_signal_connect(win->indicator_area, "draw", G_CALLBACK(on_indicator_draw), win);
    g_signal_connect(win->indicator_area, "button-press-event",
                     G_CALLBACK(on_indicator_button_press), win);
    gtk_widget_set_tooltip_text(GTK_WIDGET(win->indicator_area),
                                "Connection Status (click to check)");
    gtk_box_pack_start(win->status_bar, GTK_WIDGET(win->indicator_area), FALSE, FALSE, 4);

    /* Connect window delete event */
    g_signal_connect(win->window, "delete-event",
                     G_CALLBACK(on_window_delete_event), win);

    /* Render the initial red mic icon (also resizes window to match icon) */
    render_icon(win, "redmic.xpm");

    /* Position the window from config or defaults */
    int x = config_get_window_x(config);
    int y = config_get_window_y(config);
    if (x < 0) x = DEFAULT_WINDOW_X;
    if (y < 0) y = DEFAULT_WINDOW_Y;
    gtk_window_move(win->window, x, y);

    return win;
}

void app_window_destroy(MainWindow *win) {
    if (!win) return;

    /* Stop animations */
    app_window_stop_animation(win);
    stop_checking_blink(win);

    /* Destroy the GTK window */
    if (win->window) {
        gtk_widget_destroy(GTK_WIDGET(win->window));
    }

    /* Free cached icons (M2-002 fix) */
    if (win->cached_red_icon) {
        g_object_unref(win->cached_red_icon);
    }
    if (win->cached_green_icon) {
        g_object_unref(win->cached_green_icon);
    }

    /* Free the structure */
    free(win);
}

GtkWindow *app_window_get_gtk_window(MainWindow *win) {
    if (!win) return NULL;
    return win->window;
}

