/**
 * Render the specified icon (redmic.xpm or greenmic.xpm) onto the icon area.
 * Stores the icon dimensions for layout purposes.
 */
static void render_icon(MainWindow *win, const char *icon_name) {
    GdkPixbuf *pixbuf = load_xpm(win, icon_name);
    if (!pixbuf) {
        return;
    }

    win->icon_width = gdk_pixbuf_get_width(pixbuf);
    win->icon_height = gdk_pixbuf_get_height(pixbuf);

    /* Set the drawing area size to match the icon */
    gtk_widget_set_size_request(GTK_WIDGET(win->icon_area),
                                win->icon_width,
                                win->icon_height);
    gtk_widget_set_size_request(GTK_WIDGET(win->animation_area),
                                win->icon_width,
                                win->icon_height);

    /* Set the main window to a square based on the largest icon dimension + status bar */
    int size = (win->icon_width > win->icon_height) ? win->icon_width : win->icon_height;
    size = (size > STATUS_BAR_HEIGHT) ? size : STATUS_BAR_HEIGHT;
    int window_size = size + STATUS_BAR_HEIGHT;
    if (gtk_widget_get_realized(GTK_WIDGET(win->window))) {
        gtk_window_resize(win->window, window_size, window_size);
    } else {
        gtk_window_set_default_size(win->window, window_size, window_size);
    }

    /* Queue a redraw to render the icon */
    /* Use g_object_set_data_full with g_free to prevent leaking the previous strdup */
    g_object_set_data_full(G_OBJECT(win->icon_area), "icon_name", g_strdup(icon_name), g_free);
    gtk_widget_queue_draw(GTK_WIDGET(win->icon_area));

    g_object_unref(pixbuf);
}

/**
 * Draw callback for the icon drawing area.
 * Renders the microphone icon from XPM file.
 * When model_loading is true, overlays "WAIT" text in bold black
 * to inform the user that the system is not yet ready for dictation.
 */
static void on_icon_draw(GtkWidget *widget, cairo_t *cr, gpointer user_data) {
    MainWindow *win = (MainWindow *)user_data;
    const char *icon_name = (const char *)g_object_get_data(G_OBJECT(widget), "icon_name");

    if (!icon_name) {
        return;
    }

    // L-010 fix: Use cached icon instead of loading XPM every draw call
    GdkPixbuf *pixbuf = get_cached_icon(win, icon_name);
    if (!pixbuf) {
        return;
    }

    /* Center the icon in the drawing area */
    int area_width = gtk_widget_get_allocated_width(widget);
    int area_height = gtk_widget_get_allocated_height(widget);
    int icon_w = gdk_pixbuf_get_width(pixbuf);
    int icon_h = gdk_pixbuf_get_height(pixbuf);
    int offset_x = (area_width - icon_w) / 2;
    int offset_y = (area_height - icon_h) / 2;

    gdk_cairo_set_source_pixbuf(cr, pixbuf, offset_x, offset_y);
    cairo_paint(cr);

    g_object_unref(pixbuf);

    /* Overlay "WAIT" text when model is still loading */
    if (win->model_loading) {
        double text_x = area_width / 2.0;
        double text_y = area_height / 2.0;

        /* Use a font size proportional to the icon size */
        int font_size = (area_width < area_height) ? area_width : area_height;
        font_size = font_size / 5;  /* Roughly 20% of the smallest dimension */
        if (font_size < 10) font_size = 10;
        if (font_size > 30) font_size = 30;

        cairo_select_font_face(cr, "sans", CAIRO_FONT_SLANT_NORMAL, CAIRO_FONT_WEIGHT_BOLD);
        cairo_set_font_size(cr, font_size);

        /* Measure text for centering */
        cairo_text_extents_t te;
        cairo_text_extents(cr, "WAIT", &te);
        text_x -= te.width / 2.0 + te.x_bearing;
        text_y -= te.y_bearing + (te.height / 3.0);

        /* Draw "WAIT" in bold black */
        cairo_set_source_rgb(cr, 0.0, 0.0, 0.0);
        cairo_move_to(cr, text_x, text_y);
        cairo_show_text(cr, "WAIT");
        cairo_close_path(cr);
    }
}

