void app_window_set_state(MainWindow *win, AppState state) {
    if (!win) return;

    switch (state) {
        case STATE_IDLE:
            render_icon(win, "redmic.xpm");
            /* Update taskbar icon to red mic (cached, M2-002 fix) */
            { GdkPixbuf *icon = get_cached_icon(win, "redmic.xpm");
              if (icon) { gtk_window_set_icon(win->window, icon); g_object_unref(icon); } }
            app_window_stop_animation(win);
            gtk_widget_hide(GTK_WIDGET(win->volume_level_bar));
            gtk_level_bar_set_value(win->volume_level_bar, 0.0);
            break;
        case STATE_LISTENING:
            render_icon(win, "greenmic.xpm");
            /* Update taskbar icon to green mic (cached, M2-002 fix) */
            { GdkPixbuf *icon = get_cached_icon(win, "greenmic.xpm");
              if (icon) { gtk_window_set_icon(win->window, icon); g_object_unref(icon); } }
            gtk_widget_show(GTK_WIDGET(win->animation_area));
            gtk_widget_show(GTK_WIDGET(win->volume_level_bar));
            app_window_start_animation(win);
            app_window_start_countdown(win);
            break;
        case STATE_TRANSCRIBING:
            render_icon(win, "greenmic.xpm");
            /* Update taskbar icon to green mic (cached, M2-002 fix) */
            { GdkPixbuf *icon = get_cached_icon(win, "greenmic.xpm");
              if (icon) { gtk_window_set_icon(win->window, icon); g_object_unref(icon); } }
            app_window_stop_animation(win);
            gtk_widget_hide(GTK_WIDGET(win->animation_area));
            gtk_widget_hide(GTK_WIDGET(win->volume_level_bar));
            app_window_stop_countdown(win);
            break;
    }
}

void app_window_set_connection_status(MainWindow *win, ConnectionStatus status) {
    if (!win) return;

    /* Stop any existing blink animation */
    stop_checking_blink(win);

    /* Start blink animation for CHECKING status */
    if (status == CONNECTION_CHECKING) {
        start_checking_blink(win);
    }

    /* Queue a redraw on the indicator */
    gtk_widget_queue_draw(GTK_WIDGET(win->indicator_area));
}

void app_window_start_animation(MainWindow *win) {
    if (!win) return;

    /* Don't start if already running */
    if (win->animation_source_id != 0) {
        return;
    }

    win->wave_phase = 0.0;
    win->animation_source_id = g_timeout_add(ANIMATION_INTERVAL_MS,
                                              animation_tick,
                                              win);
}

void app_window_stop_animation(MainWindow *win) {
    if (!win) return;

    if (win->animation_source_id != 0) {
        g_source_remove(win->animation_source_id);
        win->animation_source_id = 0;
    }

    /* Clear the animation area */
    gtk_widget_queue_draw(GTK_WIDGET(win->animation_area));
    gtk_widget_hide(GTK_WIDGET(win->animation_area));
}

/* ------------------------------------------------------------------ */
/* Countdown Timer                                                     */
/* ------------------------------------------------------------------ */

static gboolean countdown_tick(gpointer user_data) {
    MainWindow *win = (MainWindow *)user_data;
    win->countdown_seconds--;
    if (win->countdown_seconds <= 0) {
        win->countdown_seconds = 0;
        gtk_label_set_text(win->countdown_label, "0");
        g_source_remove(win->countdown_source_id);
        win->countdown_source_id = 0;
        return FALSE;
    }
    gchar buf[8];
    g_snprintf(buf, sizeof(buf), "%d", win->countdown_seconds);
    gtk_label_set_text(win->countdown_label, buf);
    return TRUE; /* Continue the timer */
}

void app_window_start_countdown(MainWindow *win) {
    if (!win) return;

    /* Stop any existing countdown */
    if (win->countdown_source_id != 0) {
        g_source_remove(win->countdown_source_id);
        win->countdown_source_id = 0;
    }

    /* Get max_duration from config */
    int max_duration = 30; /* default */
    if (win->config) {
        max_duration = win->config->max_duration;
        if (max_duration <= 0) max_duration = 30;
    }

    win->countdown_seconds = max_duration;
    gchar buf[8];
    g_snprintf(buf, sizeof(buf), "%d", win->countdown_seconds);
    gtk_label_set_text(win->countdown_label, buf);
    gtk_widget_show(GTK_WIDGET(win->countdown_label));

    win->countdown_source_id = g_timeout_add_seconds(1, countdown_tick, win);
}

void app_window_stop_countdown(MainWindow *win) {
    if (!win) return;

    if (win->countdown_source_id != 0) {
        g_source_remove(win->countdown_source_id);
        win->countdown_source_id = 0;
    }

    gtk_label_set_text(win->countdown_label, "");
    gtk_widget_hide(GTK_WIDGET(win->countdown_label));
}

