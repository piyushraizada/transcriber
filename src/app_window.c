/**
 * Timer callback for the sine wave animation.
 * Increments the phase and queues a redraw.
 */
static gboolean animation_tick(gpointer user_data) {
    MainWindow *win = (MainWindow *)user_data;
    win->wave_phase += WAVE_PHASE_INCREMENT;
    gtk_widget_queue_draw(GTK_WIDGET(win->animation_area));
    return TRUE; /* Continue the timer */
}

/**
 * Draw callback for the animation drawing area.
 * Renders the sine wave using Cairo.
 */
static void on_animation_draw(GtkWidget *widget, cairo_t *cr, gpointer user_data) {
    MainWindow *win = (MainWindow *)user_data;

    /* Only draw the sine wave when animation is actively running */
    if (win->animation_source_id == 0) {
        return;
    }

    int width = gtk_widget_get_allocated_width(widget);
    int height = gtk_widget_get_allocated_height(widget);

    if (width <= 0 || height <= 0) {
        return;
    }

    double center_y = height / 2.0;

    /* Set up the sine wave style */
    cairo_set_source_rgba(cr, WAVE_COLOR_R, WAVE_COLOR_G, WAVE_COLOR_B, WAVE_COLOR_A);
    cairo_set_line_width(cr, WAVE_LINE_WIDTH);

    /* Draw the sine wave */
    cairo_move_to(cr, 0, center_y);
    for (int x = 0; x < width; x++) {
        double y = center_y + WAVE_AMPLITUDE *
                   sin(WAVE_FREQUENCY * x + win->wave_phase);
        cairo_line_to(cr, x, y);
    }
    cairo_stroke(cr);
}

