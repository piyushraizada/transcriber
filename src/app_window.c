/**
 * Timer callback for the CHECKING status blink animation.
 * Toggles the blink state and queues a redraw.
 */
static gboolean checking_blink_tick(gpointer user_data) {
    MainWindow *win = (MainWindow *)user_data;
    win->checking_blink = !win->checking_blink;
    gtk_widget_queue_draw(GTK_WIDGET(win->indicator_area));
    return TRUE; /* Continue the timer */
}

/**
 * Start the blink animation for CHECKING status.
 */
static void start_checking_blink(MainWindow *win) {
    if (win->checking_source_id == 0) {
        win->checking_blink = TRUE;
        win->checking_source_id = g_timeout_add(500, checking_blink_tick, win);
    }
}

/**
 * Stop the blink animation for CHECKING status.
 */
static void stop_checking_blink(MainWindow *win) {
    if (win->checking_source_id != 0) {
        g_source_remove(win->checking_source_id);
        win->checking_source_id = 0;
        win->checking_blink = FALSE;
    }
}

/**
 * Draw callback for the connection indicator drawing area.
 * Renders an 8x8 colored circle based on connection status.
 */
static void on_indicator_draw(GtkWidget *widget, cairo_t *cr, gpointer user_data) {
    MainWindow *win = (MainWindow *)user_data;

    int width = gtk_widget_get_allocated_width(widget);
    int height = gtk_widget_get_allocated_height(widget);

    if (width <= 0 || height <= 0) {
        return;
    }

    double cx = width / 2.0;
    double cy = height / 2.0;
    double radius = INDICATOR_SIZE / 2.0;

    ConnectionStatus status = app_get_connection_status(win->controller);

    double r, g, b;
    switch (status) {
        case CONNECTION_LOADING:
            r = COLOR_LOADING_R;
            g = COLOR_LOADING_G;
            b = COLOR_LOADING_B;
            break;
        case CONNECTION_CHECKING:
            r = COLOR_CHECKING_R;
            g = COLOR_CHECKING_G;
            b = COLOR_CHECKING_B;
            /* Blink: only draw when checking_blink is true */
            if (!win->checking_blink) {
                return;
            }
            break;
        case CONNECTION_CONNECTED:
            r = COLOR_CONNECTED_R;
            g = COLOR_CONNECTED_G;
            b = COLOR_CONNECTED_B;
            break;
        case CONNECTION_DISCONNECTED:
        default:
            r = COLOR_DISCONNECTED_R;
            g = COLOR_DISCONNECTED_G;
            b = COLOR_DISCONNECTED_B;
            break;
    }

    cairo_set_source_rgb(cr, r, g, b);
    cairo_arc(cr, cx, cy, radius, 0, 2 * G_PI);
    cairo_fill(cr);
}

