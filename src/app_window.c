/**
 * Determine the path to the assets directory.
 * Looks for assets/ relative to the executable location first,
 * then falls back to the current directory.
 */
static void get_asset_dir(MainWindow *win) {
    /* Try to get the directory of the executable */
    char exe_path[PATH_MAX];
    ssize_t len = readlink("/proc/self/exe", exe_path, sizeof(exe_path) - 1);
    if (len != -1) {
        exe_path[len] = '\0';
        char *dir = dirname(exe_path);
        snprintf(win->asset_dir, sizeof(win->asset_dir), "%s/assets", dir);
    } else {
        /* Fallback: look for assets/ relative to current directory */
        snprintf(win->asset_dir, sizeof(win->asset_dir), "./assets");
    }
}

/**
 * Load an XPM icon into a GdkPixbuf.
 * Prefers embedded XPM arrays (XPM_STATICALLY_EMBEDDED),
 * falls back to loading from disk.
 *
 * Returns a new GdkPixbuf on success, NULL on failure.
 */
static GdkPixbuf *load_xpm(MainWindow *win, const char *filename) {
    (void)win;
#ifdef XPM_STATICALLY_EMBEDDED
    /* Use embedded XPM arrays */
    if (strcmp(filename, "gear.xpm") == 0) {
        return gdk_pixbuf_new_from_xpm_data(GEAR_XPM);
    } else if (strcmp(filename, "greenmic.xpm") == 0) {
        return gdk_pixbuf_new_from_xpm_data(GREENMIC_XPM);
    } else if (strcmp(filename, "redmic.xpm") == 0) {
        return gdk_pixbuf_new_from_xpm_data(REDMIC_XPM);
    }
    return NULL;  /* Unknown XPM filename */
#else
    /* Fallback: load from disk */
    (void)win;
    char path[PATH_MAX];
    g_snprintf(path, sizeof(path), "%s/%s", win->asset_dir, filename);

    GdkPixbuf *pixbuf = gdk_pixbuf_new_from_file(path, NULL);
    if (!pixbuf) {
        /* Fallback: try relative to current directory */
        pixbuf = gdk_pixbuf_new_from_file(filename, NULL);
    }
    return pixbuf;
#endif
}

/**
 * Get a cached GdkPixbuf for the given icon name.
 * Loads and caches the pixbuf on first access, then returns a ref'd copy.
 * (M2-002 fix — avoids repeated XPM parsing on state transitions)
 */
static GdkPixbuf *get_cached_icon(MainWindow *win, const char *icon_name) {
    if (strcmp(icon_name, "redmic.xpm") == 0) {
        if (!win->cached_red_icon) {
            win->cached_red_icon = load_xpm(win, "redmic.xpm");
        }
        if (win->cached_red_icon) {
            g_object_ref(win->cached_red_icon);
            return win->cached_red_icon;
        }
    } else if (strcmp(icon_name, "greenmic.xpm") == 0) {
        if (!win->cached_green_icon) {
            win->cached_green_icon = load_xpm(win, "greenmic.xpm");
        }
        if (win->cached_green_icon) {
            g_object_ref(win->cached_green_icon);
            return win->cached_green_icon;
        }
    }
    return NULL;
}

